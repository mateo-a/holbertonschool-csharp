using System;

namespace MyMath
{
    /// <summary>
    /// Class Operations
    /// </summary>
    public class Operations
    {
        /// <summary>
        /// Method that adds 2 numbers
        /// </summary>
        /// <param name="a">An integer number "a" to be added</param>
        /// <param name="b">An integer number "b" to be added</param>
        /// <returns>Sum of a and b</returns>
        public static int Add(int a, int b)
        {
            return (a + b);
        }
    }
}
